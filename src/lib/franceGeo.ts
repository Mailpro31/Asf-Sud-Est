/** Convertit un contour [lon,lat][] en chaîne `points` SVG, mise à l'échelle. */
export function toSvgPoints(coords: [number, number][], scale = 1): string {
  return coords
    .map(([lon, lat]) => {
      const { x, y } = lonLatToXY(lon, lat);
      return `${(x * scale).toFixed(1)},${(y * scale).toFixed(1)}`;
    })
    .join(' ');
}

